package commands;

/**
 * Команда вывода всех элементов коллекции.
 * <p>
 * Реализует функциональность команды "show". Выводит в консоль строковое представление
 * всех билетов, хранящихся в текущей коллекции. Каждый билет выводится в отдельной строке
 * с полной информацией о нём.
 * </p>
 *
 * <p>Особенности работы:</p>
 * <ul>
 *   <li>Не требует аргументов</li>
 *   <li>Не изменяет коллекцию (только чтение)</li>
 *   <li>Выводит элементы в порядке их хранения в коллекции</li>
 *   <li>Если коллекция пуста, метод {@code showAll()} должен вернуть соответствующее сообщение</li>
 *   <li>Каждый элемент выводится с использованием {@link data.Ticket#toString()}</li>
 * </ul>
 *
 * @see Command
 * @see data.Ticket
 * @see data.Ticket#toString()
 */
public class ShowCommand extends Command {

    /**
     * Выполняет команду вывода всех элементов коллекции.
     * <p>
     * Процесс выполнения:
     * </p>
     * <ol>
     *   <li>Вызывает метод {@code showAll()} у менеджера коллекции</li>
     *   <li>Метод формирует строковое представление всех билетов в коллекции</li>
     *   <li>Полученная строка выводится в консоль через {@link System#out}</li>
     * </ol>
     */
    @Override
    public void execute() {
        System.out.println(collection.showAll());
    }

    /**
     * Возвращает описание команды.
     *
     * @return строковое описание функциональности команды
     */
    @Override
    public String getDescription() {
        return "Вывод всех элементов коллекции";
    }

    /**
     * Возвращает имя команды.
     *
     * @return строковое имя команды для вызова ("show")
     */
    @Override
    public String getName() {
        return "show";
    }
}