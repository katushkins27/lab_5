package commands;

import collection.TicketCollection;

/**
 * Команда очистки коллекции.
 * <p>
 * Реализует функциональность команды "clear". При выполнении удаляет все элементы
 * из текущей коллекции билетов, полностью очищая её.
 * </p>
 *
 * <p>После выполнения команды коллекция становится пустой.
 * Все ранее сохраненные данные будут потеряны (до следующего сохранения в файл).</p>
 *
 * @see Command
 */
public class ClearCommand extends Command {

    /**
     * Выполняет команду очистки коллекции.
     * <p>
     * Вызывает метод {@link TicketCollection#clearCollection()}
     * для удаления всех элементов из коллекции. После успешной очистки выводит
     * подтверждающее сообщение в консоль.
     * </p>
     *
     * <p>Команда не требует аргументов и работает одинаково как в интерактивном режиме,
     * так и в режиме выполнения скрипта.</p>
     */
    @Override
    public void execute() {
        collection.clearCollection();
        System.out.println("Коллекция очищена");
    }

    /**
     * Возвращает описание команды.
     *
     * @return строковое описание функциональности команды
     */
    @Override
    public String getDescription() {
        return "Очищение коллекции";
    }

    /**
     * Возвращает имя команды.
     *
     * @return строковое имя команды для вызова ("clear")
     */
    @Override
    public String getName() {
        return "clear";
    }
}