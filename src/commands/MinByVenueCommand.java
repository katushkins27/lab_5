package commands;

import data.Ticket;

/**
 * Команда вывода билета с минимальным местом проведения (Venue).
 * <p>
 * Реализует функциональность команды "min_by_venue". Находит в коллекции билет,
 * у которого место проведения (Venue) является минимальным согласно естественному
 * порядку сравнения объектов {@link data.Venue} (сначала по вместимости, затем по названию).
 * </p>
 *
 * <p>Особенности работы:</p>
 * <ul>
 *   <li>Не требует аргументов</li>
 *   <li>Не изменяет коллекцию (только чтение)</li>
 *   <li>Сравнение происходит по полю {@code venue} билета</li>
 *   <li>Если коллекция пуста, выводит соответствующее сообщение</li>
 *   <li>Если у нескольких билетов одинаковый минимальный Venue, выводится первый найденный</li>
 * </ul>
 *
 *
 * @see Command
 * @see Ticket
 * @see data.Venue
 */
public class MinByVenueCommand extends Command {

    /**
     * Выполняет команду поиска билета с минимальным местом проведения.
     * <p>
     * Процесс выполнения:
     * </p>
     * <ol>
     *   <li>Вызывает метод {@code getMinByVenue()} у менеджера коллекции</li>
     *   <li>Метод перебирает все билеты и находит билет с минимальным Venue</li>
     *   <li>Если билет найден, выводит его строковое представление</li>
     *   <li>Если коллекция пуста, выводит сообщение "Коллекция пустая"</li>
     * </ol>
     *
     */
    @Override
    public void execute() {
        Ticket ticket = collection.getMinByVenue();
        if (ticket == null) {
            System.out.println("Коллекция пустая");
        } else {
            System.out.println(ticket);
        }
    }

    /**
     * Возвращает описание команды.
     *
     * @return строковое описание функциональности команды
     */
    @Override
    public String getDescription() {
        return "Билет с минимальным Venue";
    }

    /**
     * Возвращает имя команды.
     *
     * @return строковое имя команды для вызова ("min_by_venue")
     */
    @Override
    public String getName() {
        return "min_by_venue";
    }
}