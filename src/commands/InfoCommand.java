package commands;

/**
 * Команда вывода информации о коллекции.
 * <p>
 * Реализует функциональность команды "info". Выводит подробную информацию о текущем состоянии
 * коллекции билетов: тип коллекции, дата инициализации, количество элементов и другие
 * характеристики, предоставляемые менеджером коллекции.
 * </p>
 *
 * <p>Особенности работы:</p>
 * <ul>
 *   <li>Не требует аргументов</li>
 *   <li>Не изменяет коллекцию (только чтение)</li>
 *   <li>Выводит информацию в формате, определяемом методом {@code getInfo()} коллекции</li>
 *   <li>Работает даже с пустой коллекцией</li>
 * </ul>
 *
 * @see Command
 * @see collection.TicketCollection#getInfo()
 */
public class InfoCommand extends Command {

    /**
     * Выполняет команду вывода информации о коллекции.
     */
    @Override
    public void execute() {
        System.out.println(collection.getInfo());
    }

    /**
     * Возвращает описание команды.
     *
     * @return строковое описание функциональности команды
     */
    @Override
    public String getDescription() {
        return "Вывод инфо о коллекции";
    }

    /**
     * Возвращает имя команды.
     *
     * @return строковое имя команды для вызова ("info")
     */
    @Override
    public String getName() {
        return "info";
    }
}