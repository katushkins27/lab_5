package commands;

import data.Ticket;

/**
 * Команда удаления первого элемента коллекции.
 * <p>
 * Реализует функциональность команды "remove_head". Удаляет первый элемент из коллекции
 * (в порядке, определяемом текущей сортировкой коллекции).
 * </p>
 *
 * <p>Особенности работы:</p>
 * <ul>
 *   <li>Не требует аргументов</li>
 *   <li>Удаляет первый элемент коллекции</li>
 *   <li>Если коллекция пуста, метод {@code removeHead()} должен корректно обрабатывать эту ситуацию</li>
 *   <li>После удаления все остальные элементы смещаются (первым становится следующий)</li>
 * </ul>
 *
 * @see Command
 */
public class RemoveHeadCommand extends Command {

    /**
     * Выполняет команду удаления первого элемента коллекции.
     * <p>
     * Процесс выполнения:
     * </p>
     * <ol>
     *   <li>Вызывает метод {@code removeHead()} у менеджера коллекции</li>
     *   <li>Метод удаляет первый элемент из коллекции (если коллекция не пуста)</li>
     *   <li>Выводит подтверждающее сообщение "Удален первый элемент коллекции"</li>
     * </ol>
     */
    @Override
    public void execute() {
        Ticket ticket = collection.head();
        if (ticket == null) {
            System.out.println("Коллекция пустая");
        } else {
            collection.removeHead();
            System.out.println("Удален первый элемент коллекции");
        }
    }

    /**
     * Возвращает описание команды.
     *
     * @return строковое описание функциональности команды
     */
    @Override
    public String getDescription() {
        return "Удаление первого элемента коллекции";
    }

    /**
     * Возвращает имя команды.
     *
     * @return строковое имя команды для вызова ("remove_head")
     */
    @Override
    public String getName() {
        return "remove_head";
    }
}