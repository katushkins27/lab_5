package commands;

/**
 * Команда выхода из приложения без сохранения.
 * <p>
 * Реализует функциональность команды "exit". При выполнении выводит сообщение о завершении
 * работы и завершает выполнение программы. Важно: команда НЕ сохраняет текущее состояние
 * коллекции в файл.
 * </p>
 *
 * <p>Особенности работы:</p>
 * <ul>
 *   <li>Не требует аргументов</li>
 *   <li>Не сохраняет изменения</li>
 *   <li>После вызова команды приложение завершает работу</li>
 *   <li>Все несохраненные изменения будут потеряны</li>
 * </ul>
 *
 * <p>Предупреждение: используйте эту команду только если вы уверены, что не нужно
 * сохранять изменения, или если сохранение было выполнено ранее командой "save".</p>
 *
 * @see Command
 * @see SaveCommand
 */
public class ExitCommand extends Command {

    /**
     * Выполняет команду выхода из приложения.
     * <p>
     * Выводит предупреждающее сообщение о том, что сохранение не производится.
     * </p>
     * <p>Метод возвращает управление, а завершение программы происходит
     * в вызывающем коде при проверке флага или получении сигнала о выходе.</p>
     */
    @Override
    public void execute() {
        System.out.println("Завершение без сохранения");
    }

    /**
     * Возвращает описание команды.
     *
     * @return строковое описание функциональности команды
     */
    @Override
    public String getDescription() {
        return "Выход без сохранения в файл";
    }

    /**
     * Возвращает имя команды.
     *
     * @return строковое имя команды для вызова ("exit")
     */
    @Override
    public String getName() {
        return "exit";
    }
}